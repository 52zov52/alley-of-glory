// Helper function to split heroes into groups for trees
export function getHeroGroups(heroes: Hero[], groupSize: number = 4): Hero[][] {
  const groups: Hero[][] = []
  for (let i = 0; i < heroes.length; i += groupSize) {
    groups.push(heroes.slice(i, i + groupSize))
  }
  return groups
}